function readmeMd(inputs: WorkspaceFileInputs): string {
  return `# ${inputs.name}

An [openRupiv](https://github.com/openrupiv) workspace. Apps here are
generated as a reviewable declarative spec plus a deterministic projection
(ADR-0001) and served by the shared runtime engine (ADR-0004) — the LLM
never writes application code.

## Quickstart: described app running locally in under 10 minutes

Prerequisites: Docker with Compose v2, git, an openRupiv monorepo checkout
(v0 runs the CLI from it — its path is recorded as \`OPENRUPIV_REPO\` in
this workspace's \`.env\`), and an Anthropic API key.

**1. Generate the app.** From the monorepo checkout:

\`\`\`sh
export ANTHROPIC_API_KEY="<your key>"   # read from the environment only; never written to disk
corepack pnpm --filter @openrupiv/cli exec tsx src/main.ts \\
  generate "an approval workflow for vendor onboarding with 4-eyes review" \\
  --dir "<path to this workspace>"
\`\`\`

This writes \`./app\` (spec.json, SQL migration, docs, standalone tests)
and commits it to this repository with your git identity.

**2. One-time host setup.** The runtime and your browser must both reach
the Dex issuer under the same name (\`http://dex:5556\`):

\`\`\`sh
echo "127.0.0.1 dex" | sudo tee -a /etc/hosts
\`\`\`

**3. Start the stack** (first run pulls postgres/dex images and builds the
runtime image from your monorepo checkout):

\`\`\`sh
docker compose up --build
\`\`\`

**4. Log in.** Open <http://localhost:3000>, sign in as
\`${DEV_USER_EMAIL}\` / \`${DEV_USER_PASSWORD}\`, and use the app.
A second user, \`${DEV_USER2_EMAIL}\` (same password), exists so
two-person approval steps (4-eyes) can be completed: approve once as each
user. In dev mode every user is granted all app roles (ADR-0005).

## What is running

| Service  | Image / build                              | Port | Purpose                          |
|----------|--------------------------------------------|------|----------------------------------|
| postgres | \`${POSTGRES_IMAGE}\` (volume: \`pgdata\`)   | —    | App persistence                  |
| dex      | \`${DEX_IMAGE}\`                             | 5556 | DEV-ONLY OIDC identity provider  |
| runtime  | built from \`$OPENRUPIV_REPO\` (packages/runtime/Dockerfile) | 3000 | Serves \`./app\` |

## DEV-ONLY credentials — read before deploying anything

Everything below is **public knowledge** and exists only so local
development works with zero identity configuration (ADR-0002):

| What                 | Value                                        |
|----------------------|----------------------------------------------|
| Login users          | \`${DEV_USER_EMAIL}\`, \`${DEV_USER2_EMAIL}\` (both: \`${DEV_USER_PASSWORD}\`) |
| OIDC client          | \`${DEV_OIDC_CLIENT_ID}\` / \`${DEV_OIDC_CLIENT_SECRET}\` |
| Postgres             | \`openrupiv\` / \`openrupiv-dev-password\`    |

The runtime **refuses to start** with the bundled dev client secret unless
\`OPENRUPIV_DEV_MODE=true\` is set — the compliant path is the default path.

**Going to production** is one seam: delete the \`dex\` service, point
\`OIDC_ISSUER\` / \`OIDC_CLIENT_ID\` / \`OIDC_CLIENT_SECRET\` at your
organization's IdP, set a fresh \`SESSION_SECRET\`, and remove
\`OPENRUPIV_DEV_MODE\`. There is no password table to migrate — openRupiv
is OIDC-only from the first commit (ADR-0003).

## Workspace layout

\`\`\`
openrupiv.yaml        workspace config (spec v0.1)
docker-compose.yaml   local dev stack (postgres + dex + runtime)
dex/config.yaml       DEV-ONLY IdP configuration
.env                  SESSION_SECRET + SANDBOX_TOKEN + OPENRUPIV_REPO (gitignored)
app/                  generated app — appears after \`openrupiv generate\`
\`\`\`

Regenerating: run \`openrupiv generate\` again. The \`./app\` directory is
rewritten from the new spec and committed, so every change is reviewable
git history.
`;
}

/**
 * All files `openrupiv new` writes, sorted by path. Pure function —
 * determinism is tested.
 */
export function workspaceFiles(inputs: WorkspaceFileInputs): WorkspaceFile[] {
  const files: WorkspaceFile[] = [
    { path: ".env", contents: envFile(inputs) },
    { path: ".gitignore", contents: gitignore() },
    { path: "README.md", contents: readmeMd(inputs) },
    { path: "dex/config.yaml", contents: dexConfigYaml() },
    { path: "docker-compose.yaml", contents: dockerComposeYaml(inputs) },
    { path: "openrupiv.yaml", contents: openrupivYaml() },
  ];
  return files.sort((a, b) => (a.path < b.path ? -1 : a.path > b.path ? 1 : 0));
}
